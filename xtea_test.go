package tea

import (
	"bytes"
	"testing"
)

func TestXTinyEncryptionAlgorithm_Encrypt(t *testing.T) {
	out := make([]byte, 8)

	c, err := NewXTEA([]byte{0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xAA, 0xBB, 0xCC, 0xDD, 0xEE, 0xFF})
	if err != nil {
		t.Error(err)
	}

	c.Encrypt(out, []byte{0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08})

	if bytes.Compare(out, []byte{0xDC, 0xDD, 0x7A, 0xCD, 0xC1, 0x58, 0x4B, 0x79}) != 0 {
		t.Error(err)
	}
}

